-- --------------------e_updateSaleMonth---------------------------------
-- Actualizara cada 1 del  mes el product rotation month 
-- ----------------------------------------------------------------------

DELIMITER $$
CREATE EVENT e_updateSaleMonth
ON SCHEDULE EVERY 1 MONTH
STARTS '2016-01-01 12:00:00' ENABLE
DO 
BEGIN
UPDATE product SET pro_rotationMonth=0;
END; $$


